// increment and decrement
#include<iostream>
using namespace std;
int main(){
    int x = 1;
    cout<<x<<endl;
    x++; // post increment
    cout<<x<<endl;
    ++x; // per increment
    cout<<x<<endl;
    x--; // post decrement
    cout<<x<<endl;
    --x; // per decrement
    cout<<x;

}